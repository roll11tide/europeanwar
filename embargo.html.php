                    <p>The Embargo of 1807 was a retallation against the British for not only blocking trade with neutral nations, but also impressing American sailors under the guise that they were British citizens. The rage that this created within the US was parallel to that invoked at Lexington at the start of the Revolutionary War. American sovereignty was being disrespected.</p>

                    <p>Jefferson's answer to the trade issues with Britain was an embargo against overseas trade with Britain and France. This was intended to create economic hardship on the European countries, who were then at war for control of Europe. The embargo was largely ineffective, as American merchants could evade the embargo using loopholes in the legistlation and through poor enforcement.</p>

                    <h2 class="section-heading">The Chesapeake-Leopard Affair</h2>

                    <p>The Chesapeake-Leopard Affair was a confrontation between was a confrontation between the British HMS Leopard and the American USS Chesapeake. The commanding officer of the HMS Leopard, Salusbury Humphreys, demanded to search the USS Chesapeake for three suspected British deserters. James Barron, the American commander, refused. As a result of the American denial to board, the Leopard opened fire on the USS Chesapeake, allowing the American ship to fire only a single round and forcing them to surrender.</p>

                    <p>This enraged many Americans and brought a heightened rage to the public. They felt their rights as a free nation were being violated by their once-overlord and were threatened by the plundering of their overseas trade by England and France. The justifications for war were stacking one on top of the other, and this landed at the top of the pile.</p>

                    <blockquote>"We consider a neutral flag, on the high seas, as a safeguard to those sailing under it. Great Britain, on the contrary, asserts a right to search for, and seize, her own subjects; and under that cover, as cannot happen, are often seized and taken off, citizens of the United States and citizens or subjects of other neutral countries, navigating the high seas, under the protection of the American flag." -James Madison to Mr. Monroe, 5th February 1806</blockquote>

                    <h2 class="section-heading">Long-Term Effects of the Embargo</h2>

                    <p>The embargo hurt the US as much as it did Britain and France. Britain anticipated the hardships and set up a new South American export market, which their merchants were happy to operate in without the presence of American traders. Jefferson acted contrary to the principles of his party, using extraordinarily broad federal measures to enforce his failing embargo. The embargo also stirred up public resentment and caused a general feeling of unrest.</p>

